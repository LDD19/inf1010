#ifndef TYPE_PIXEL_H
#define TYPE_PIXEL_H

/**
 * Cette enumération définit les types de pixel supportés.
 */
enum TypePixel {
    /** Un pixel noir ou blanc. */
    NoireBlanc,

    /** Un pixel gris. */
    NuanceDeGris,

    /** Un pixel d'une couleur générale. */
    Couleur
};

#endif
